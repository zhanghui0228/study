'''
    对多核的利用
        单核CPU系统中，不存在真正的并发
        GIL ----全局解释器锁
        GIL只是强制在任何时候只有一个线程可以执行python代码
        I/O密集型应用与CPU密集型应用
        
    GIL执行顺序：
        1、设置GIL
        2、切换进一个线程去执行
        3、执行下面操作之一：
            指定数量的字节码指令
            线程主动让出控制权（可以调用time.sleep(0)来完成）
        4、把线程设置回睡眠状态（切换出线程）
        5、解锁GIL
        6、重复上面步骤
'''

